public class KompNaOsoba {
    public static void main(String[] args) {

        int komputer = 17;
        int osoba = 11;

        double kompNaOsoba;

        kompNaOsoba = (double)komputer / osoba;

        System.out.println("Ilośc komputerów na osobę: " +
                kompNaOsoba);
    }
}
